public class Switch{
    public static void main (String[] args)
    {
        int i=4;
        switch(i)
        {
            case 0:
            System.out.println("Zero");
            break;
            case 1:
            System.out.println("one");
            break;
            case 2:
            System.out.println("two");
            break;
            case 3:
            System.out.println("three");
            break;
            default:
            System.out.println("some number");

        }
    }
}